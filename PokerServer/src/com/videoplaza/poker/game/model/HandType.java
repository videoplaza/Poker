package com.videoplaza.poker.game.model;

public enum HandType {
   STRAIGHT_FLUSH(80000000), FOUR_OF_A_KIND(70000000), FULL_HOUSE(60000000), FLUSH(50000000), STRAIGHT(40000000), THREE_OF_A_KIND(30000000), TWO_PAIRS(20000000), PAIR(
         10000000), HIGH_CARD(0);

   private int value;

   private HandType(int value) {
      this.value = value;
   }

   public int getValue() {
      return value;
   }
}